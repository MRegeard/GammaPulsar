import matplotlib.pyplot as plt

# Données à tracer
x = [1, 2, 3, 4, 5]
y = [2, 4, 6, 8, 10]

# Créer le graphique
plt.plot(x, y)

# Ajouter un titre
plt.title("Mon graphique linéaire")

# Ajouter des étiquettes d'axe
plt.xlabel("Axe des x")
plt.ylabel("Axe des y")

# Afficher le graphique
plt.show()

